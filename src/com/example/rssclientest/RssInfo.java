package com.example.rssclientest;

public class RssInfo {
	private String postThumbUrl;
	private String postTitle;
	private String postLink;
	private String postContent;
	private String postGuid;
	private String postData;
	
	public String getPostData() {
		return postData;
	}
	public void setPostData(String postData) {
		this.postData = postData;
	}
	public String getPostThumbUrl() {
		return postThumbUrl;
	}
	public void setPostThumbUrl(String postThumbUrl) {
		this.postThumbUrl = postThumbUrl;
	}
	public String getPostTitle() {
		return postTitle;
	}
	public void setPostTitle(String postTitle) {
		this.postTitle = postTitle;
	}
	public String getPostLink() {
		return postLink;
	}
	public void setPostLink(String postLink) {
		this.postLink = postLink;
	}
	public String getPostContent() {
		return postContent;
	}
	public void setPostContent(String postContent) {
		this.postContent = postContent;
	}
	public String getPostGuid() {
		return postGuid;
	}
	public void setPostGuid(String postGuid) {
		this.postGuid = postGuid;
	}
}
